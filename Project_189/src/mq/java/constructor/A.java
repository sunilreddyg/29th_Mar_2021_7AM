package mq.java.constructor;

public class A 
{

	public A()    //This is constructor of class
	{
		System.out.println("constructor executed");
	}
	
	

	public static void main(String[] args) 
	{
		
		/*
		 * Syntax to call constructor of class
		 * 		new Classname();
		 */
		new A();

	}

}
